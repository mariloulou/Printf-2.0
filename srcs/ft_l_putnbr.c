#include "../includes/libftprintf.h"

void	ft_l_putnbr(long int c)
{
	if (c < 0)
	{
		c = -c;
	}
	if (c >= 10)
	{
		ft_putnbr(c / 10);
		ft_putnbr(c % 10);
	}
	else
		ft_putchar(c + '0');
}
